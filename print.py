# 1- No more than 1 instruction in a line in Python.So for example we can do:

print("1.Hello, \nfuture python programmer")

# 2- Python combines multiple values in one
print("2.Hello", "future", "python", "programmer")

# 3- we can use keyword arguments, for example 'end'

print("3.Hello", end="!")
print("3.future python programmer")

# 4- Another keyword argument is sep which allows you to control how python separates the output of arguments
# which is space by default.

print("4.Hello", "future", "python", "programmer", sep="-")

# 5- So we can combine the 2 of them.

print("5.So” , “enjoying python”,sep=”,”,end=”!”")
